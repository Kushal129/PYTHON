# Write a Python program to accept two Integers and check if they are equal.
def main():
    # Get user input for the two integers
    num1 = int(input("Enter the first integer: "))
    num2 = int(input("Enter the second integer: "))

    # Check if the two integers are equal
    if num1 == num2:
        print("The two integers are equal.")
    else:
        print("The two integers are not equal.")

# Call the main function
main()
